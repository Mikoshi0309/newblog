    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $comment = Comment::find($id);
        $re = $comment->delete();
        if($re){
            Article::where('id',$comment->article_id)->decrement('comment_count');
            return $data = [
                'status' => 0,
                'message' => '删除成功'
            ];
        }else{
            return $data = [
                'status' => 1,
                'message' => '删除失败'
            ];
        }
        return $data;
    }
